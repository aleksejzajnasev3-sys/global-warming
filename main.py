def decision(prompt, choices):
    for i, choice in enumerate(choices):
        print(f"{i+1}. {choice}")
    answer = int(input("Ваш выбор: "))
    while answer < 1 or answer > len(choices):
        answer = int(input("Неправильный номер. Попробуйте ещё раз: "))
    return answer

