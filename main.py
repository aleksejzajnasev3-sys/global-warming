def decision(prompt, choices):
    start_time = time.time()  
    for i, choice in enumerate(choices):
        print(f"{i+1}. {choice}")
    answer = int(input("Ваш выбор: "))
    elapsed_time = round(time.time() - start_time, 2)  
    print(f"Время на размышление: {elapsed_time:.2f} секунд")
    while answer < 1 or answer > len(choices):
        answer = int(input("Неправильный номер. Попробуйте ещё раз: "))
    return answer, elapsed_time

def calculate_bonus(elapsed_time):
    max_bonus_time = 10  
    bonus_points = max(max_bonus_time - elapsed_time, 0) / max_bonus_time * 100
    return round(bonus_points)

